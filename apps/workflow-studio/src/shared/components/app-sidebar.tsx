"use client"

import * as React from "react"
import { Link, useMatchRoute } from "@tanstack/react-router"
import { Moon, Sun, Hexagon, Workflow, FolderOpen } from "lucide-react"

import { Avatar, AvatarFallback } from "@/shared/components/ui/avatar"
import { useTheme } from "@/shared/components/theme-provider"
import {
  Sidebar,
  SidebarContent,
  SidebarFooter,
  SidebarHeader,
  SidebarMenu,
  SidebarMenuButton,
  SidebarMenuItem,
  SidebarRail,
  SidebarGroup,
  SidebarGroupContent,
} from "@/shared/components/ui/sidebar"

export function AppSidebar({ ...props }: React.ComponentProps<typeof Sidebar>) {
  const matchRoute = useMatchRoute()
  const isWorkflowsActive = matchRoute({ to: '/workflows' })
  const isEditorActive = matchRoute({ to: '/editor' })
  const { theme, setTheme } = useTheme()

  const toggleTheme = () => {
    setTheme(theme === 'dark' ? 'light' : 'dark')
  }

  return (
    <Sidebar collapsible="icon" variant="floating" {...props}>
      <SidebarHeader>
        <SidebarMenu>
          <SidebarMenuItem>
            <SidebarMenuButton size="lg" tooltip="Command Studio">
              <div className="flex aspect-square size-8 items-center justify-center rounded-lg bg-gradient-to-br from-gray-300 via-gray-400 to-gray-500 dark:from-gray-500 dark:via-gray-600 dark:to-gray-700 shadow-sm">
                <Hexagon className="size-4 text-white dark:text-gray-200" strokeWidth={2.5} fill="currentColor" fillOpacity={0.2} />
              </div>
              <div className="grid flex-1 text-left leading-tight">
                <div className="flex items-center gap-1">
                  <span className="text-sm font-semibold tracking-tight text-gray-700 dark:text-gray-200">Command</span>
                  <span className="text-xs font-bold px-1.5 py-0.5 rounded bg-gradient-to-r from-gray-500 to-gray-600 dark:from-gray-400 dark:to-gray-500 text-white shadow-sm">Studio</span>
                </div>
                <span className="truncate text-[10px] text-muted-foreground/60 italic">by Luna</span>
              </div>
            </SidebarMenuButton>
          </SidebarMenuItem>
        </SidebarMenu>
      </SidebarHeader>
      <SidebarContent>
        <SidebarGroup>
          <SidebarGroupContent>
            <SidebarMenu>
              <SidebarMenuItem>
                <SidebarMenuButton asChild isActive={!!isWorkflowsActive} tooltip="Workflows">
                  <Link to="/workflows">
                    <FolderOpen />
                    <span>Workflows</span>
                  </Link>
                </SidebarMenuButton>
              </SidebarMenuItem>
              <SidebarMenuItem>
                <SidebarMenuButton asChild isActive={!!isEditorActive} tooltip="Editor">
                  <Link to="/editor">
                    <Workflow />
                    <span>Editor</span>
                  </Link>
                </SidebarMenuButton>
              </SidebarMenuItem>
            </SidebarMenu>
          </SidebarGroupContent>
        </SidebarGroup>
      </SidebarContent>
      <SidebarFooter>
        <SidebarMenu>
          <SidebarMenuItem>
            <SidebarMenuButton onClick={toggleTheme} tooltip={theme === 'dark' ? 'Switch to light mode' : 'Switch to dark mode'}>
              <Sun className="h-4 w-4 scale-100 rotate-0 transition-all dark:scale-0 dark:-rotate-90" />
              <Moon className="absolute h-4 w-4 scale-0 rotate-90 transition-all dark:scale-100 dark:rotate-0" />
              <span>{theme === 'dark' ? 'Dark' : 'Light'}</span>
            </SidebarMenuButton>
          </SidebarMenuItem>
          <SidebarMenuItem>
            <SidebarMenuButton size="lg" tooltip="Profile">
              <Avatar className="h-8 w-8 rounded-lg">
                <AvatarFallback className="rounded-lg">SG</AvatarFallback>
              </Avatar>
              <div className="grid flex-1 text-left text-sm leading-tight">
                <span className="truncate font-medium">Saketh G</span>
                <span className="truncate text-xs">saketh@example.com</span>
              </div>
            </SidebarMenuButton>
          </SidebarMenuItem>
        </SidebarMenu>
      </SidebarFooter>
      <SidebarRail />
    </Sidebar>
  )
}
